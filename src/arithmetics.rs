fn main() {
    let x = 200;
    let y = 2;

    // Addition
    let sum = x + y;
    println!("{} + {} = {}", x, y, sum);

    // Subtraction
    let difference = x - y;
    println!("{} - {} = {}", x, y, difference);

    // Multiplication
    let product = x * y;
    println!("{} * {} = {}", x, y, product);

    // Division
    let quotient = x / y;
    println!("{} / {} = {}",x, y, quotient);
}